export const commonIngredients: string[] = Object.values(ingredientCategories).flat();

export const findMatchingIngredients = (query: string): string[] => {
	const normalizedQuery = query.toLowerCase().trim();
	if (!normalizedQuery) return [];
	
	return commonIngredients
		.filter(ingredient => ingredient.toLowerCase().includes(normalizedQuery))
		.slice(0,8);
};